package com.example.rodiziocarro.domain.dto;

public class AnoDTO {

	private String nome;
	private String codigo;
	
	public AnoDTO() {
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getCodigo() {
		return codigo;
	}

	public void setCodigo(String codigo) {
		this.codigo = codigo;
	}
	
}
