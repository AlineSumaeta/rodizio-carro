package com.example.rodiziocarro.domain.dto;

public class ModeloDTO {

	private String nome;
	private Integer codigo;
	
	public ModeloDTO(){
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public Integer getCodigo() {
		return codigo;
	}

	public void setCodigo(Integer codigo) {
		this.codigo = codigo;
	}
	
	
}
